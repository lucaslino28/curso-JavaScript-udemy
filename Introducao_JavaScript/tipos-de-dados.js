// tipos primitivos

// numero - number
let numero = 10;
let numero2 = 9.5;

console.log(typeof numero);
console.log(typeof numero2);

// string
let nome = "Lucas";

console.log(typeof nome);

// boolean
let ligado = true;

console.log(typeof ligado);

// undefined - não tem valor definido.
let teste = undefined;
let teste2;

console.log(typeof teste);
console.log(typeof teste2);

// null - guarda a váriavel, mas deixa o espaço reservado. Para futuramente colocar valor.
let time = null;

console.log(typeof time);

// object
let objeto = {}

console.log(typeof objeto);

// function
let funcao = () => {};

console.log(typeof funcao);